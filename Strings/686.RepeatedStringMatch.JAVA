class Solution  
{
    public int repeatedString(String a, String b)
    {
        int c = 1;
        StringBuilder s = new StringBuilder(a);
        while(sb.length()<b.length())
        {
            sb.append(a);
            c++;
        }
        if(sb.indexOf(b)>=0)
        returnc ;
        if(sb.append(a).indexOf(b)>=0)
        return c+1;
    }
}